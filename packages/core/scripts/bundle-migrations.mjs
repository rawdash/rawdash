#!/usr/bin/env node
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const MIGRATIONS_DIR = resolve(__dirname, '..', 'migrations');
const OUTPUT_FILE = resolve(
  __dirname,
  '..',
  'src',
  'db',
  'migrations-bundle.ts',
);

const files = readdirSync(MIGRATIONS_DIR)
  .filter((f) => f.endsWith('.sql'))
  .sort();

const entries = files.map((file) => {
  const tag = file.replace(/\.sql$/, '');
  const sql = readFileSync(join(MIGRATIONS_DIR, file), 'utf8');
  const statements = sql
    .split('--> statement-breakpoint')
    .map((s) => s.trim())
    .filter((s) => s.length > 0);
  return { tag, statements };
});

const body = `// AUTO-GENERATED by scripts/bundle-migrations.mjs — do not edit by hand.
// Regenerate with: pnpm db:bundle (or pnpm db:generate).

export interface BundledMigration {
  tag: string;
  statements: string[];
}

export const MIGRATIONS: readonly BundledMigration[] = ${JSON.stringify(entries, null, 2)} as const;
`;

writeFileSync(OUTPUT_FILE, body);
console.log(`Wrote ${entries.length} migrations to ${OUTPUT_FILE}`);
